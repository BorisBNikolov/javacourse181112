package com.musala.javacourse181112;

public class FutureAgeCalculator {
    public static void main(String[] args) {
        int age = 25;
        System.out.println("The age that you inputted is " + age);
        int add = 6;
        System.out.println("The age that you want to add is " + add);
        add = add + age;
        System.out.println("Your future age is " + add);
        System.exit(0);
    }
}
